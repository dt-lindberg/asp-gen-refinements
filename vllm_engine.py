"""vLLM engine for batched local inference with Qwen3-30B-A3B."""

import os
import re
import time

from logger import setup_logging, get_logger
from config import (
    MODEL_PATH,
    SEED,
    THINKING,
    MAX_TOKENS,
    # THINK_END_TOKEN_ID,
    MAX_MODEL_LEN,
    MAX_NUM_BATCHED_TOKENS,
    MAX_NUM_SEQS,
    TEMPERATURE,
    GPU_MEMORY_UTILIZATION,
    TOP_P,
    TOP_K,
    MIN_P,
    THINKING_OVERFLOW,
)

setup_logging(log_level=os.getenv("LOG_LEVEL", "debug"))
logger = get_logger(__name__)


def _trim_extra_turn(text: str) -> str:
    """Cut off any second assistant turn that slipped through the stop rules.

    When the model skips <|im_end|> it emits one of these prefixes to start
    a new turn.  We cut at the earliest occurrence so only the first response
    reaches downstream parsing.
    """
    markers = [
        "\n<|im_start|>assistant\n",
        "\nassistant\n<think>",
        "\nassistant\n\n<think>",
    ]
    cut = None
    for m in markers:
        i = text.find(m)
        if i != -1:
            cut = i if cut is None else min(cut, i)
    return text[:cut].rstrip() if cut is not None else text


def _split_thinking(text):
    """Split raw vLLM output into (thinking, response).

    ASSUMES:
    * thinking is really enabled, a 'complete' thinking block is
        <think>...</think>, anything else is incomplete thinking.
    * response: remaining text after stripping the thinking block

    Handles two cases:
    1. Full block in output: <think>...</think>response
    2. <think> was prepended to prompt, so output starts directly with thinking
       content and ends with </think>response (no opening tag in output).
    3. No </think> is found, that means the thinking never ended. To avoid
       overflow, we return a semi-empty response in this case.
    """
    # Case 1:
    thinking_re = re.compile(r"<think>(.*?)</think>", re.DOTALL)
    match = thinking_re.search(text)
    if match:
        thinking = match.group(1).strip()
        response = thinking_re.sub("", text).strip()
        return thinking, response

    # Case 2:
    # <think> was in the prompt; output begins with thinking content directly
    end_idx = text.find("</think>")
    if end_idx != -1:
        thinking = text[:end_idx].strip()
        response = text[end_idx + len("</think>") :].strip()
        return thinking, response
    else:
        # Case 3: model exhausted its token budget inside the thinking block.
        # Return the sentinel so downstream code can register this as a distinct error.
        return text, THINKING_OVERFLOW


class VLLMEngine:
    def __init__(
        self,
        max_tokens=MAX_TOKENS,
        max_model_len=MAX_MODEL_LEN,
        max_num_batched_tokens=MAX_NUM_BATCHED_TOKENS,
        max_num_seqs=MAX_NUM_SEQS,
        temperature=TEMPERATURE,
    ):
        """
        * max_tokens: the maximum number of output tokens that can be generated by a single sequence.
        * max_model_len: the maximum number of input+output tokens a single sequence can occupy.
            - max_model_len * max_num_seqs = #maximum input+output tokens in batch.
            - This is essentially what might blow the KV-cache.
        * max_num_batched_tokens: the maximum number of input tokens for a full batch.
        * max_num_seqs: the maximum batch size.
        """
        from vllm import LLM, SamplingParams

        # # Use think logits processor to enforce thinking budget
        # # FQCN string: vLLM loads the class by importing the module
        # logits_processors = (
        #     ["think_logits_processor:ThinkLogitsProcessor"] if THINKING else None
        # )

        logger.info(f"Loading model from {MODEL_PATH}")
        t0 = time.perf_counter()
        self.llm = LLM(
            model=MODEL_PATH,
            max_model_len=max_model_len,
            max_num_seqs=max_num_seqs,
            max_num_batched_tokens=max_num_batched_tokens,
            gpu_memory_utilization=GPU_MEMORY_UTILIZATION,
            seed=SEED,
            # logits_processors=logits_processors,
        )
        logger.info(f"Model loaded in {time.perf_counter() - t0:.2f}s")

        self.tokenizer = self.llm.get_tokenizer()
        self._debug_tokenizer()

        im_end_ids = self.tokenizer.encode("<|im_end|>", add_special_tokens=False)
        eos_id = getattr(self.tokenizer, "eos_token_id", None)
        stop_token_ids = []
        if len(im_end_ids) == 1:
            stop_token_ids.append(im_end_ids[0])
        if eos_id is not None and eos_id not in stop_token_ids:
            stop_token_ids.append(eos_id)

        self.sampling_params = SamplingParams(
            temperature=temperature,
            max_tokens=max_tokens,
            top_p=TOP_P,
            top_k=TOP_K,
            min_p=MIN_P,
            stop=[
                "<|im_end|>",
                "<|im_start|>assistant",
                "assistant\n<think>",
                "assistant\n\n<think>",
            ],
            stop_token_ids=stop_token_ids or None,
        )

    def _debug_tokenizer(self):
        """Log how the tokenizer round-trips key special strings.

        Runs once at startup so we can verify whether <|im_end|> encodes to a
        single visible token or a special-token ID that decodes to empty string.
        That determines whether stop= (string match) or stop_token_ids= (ID
        match) is the effective guard.
        """
        candidates = ["<|im_end|>", "<|im_start|>", "assistant", "<think>", "</think>"]
        for s in candidates:
            ids = self.tokenizer.encode(s, add_special_tokens=False)
            decoded = self.tokenizer.decode(ids)
            logger.info("Tokenize %r -> ids=%s -> decode=%r", s, ids, decoded)
        eos_id = getattr(self.tokenizer, "eos_token_id", None)
        logger.info("eos_token_id=%r", eos_id)

    def _log_loop_tail(self, text: str, token_ids, idx: int):
        """Warn when a generation looks like it looped."""
        tail_ids = list(token_ids[-20:])
        tail_text = self.tokenizer.decode(tail_ids)
        logger.warning(
            "Loop Out %d | assistant_ct=%d | raw_tail=%r | tail_ids=%s | tail_text=%r",
            idx,
            text.count("assistant"),
            text[-300:],
            tail_ids,
            tail_text,
        )

    def generate_batch(self, messages_list):
        """Generate responses for a batch of conversations.

        Args:
            messages_list: list of conversations, each a list of role/content dicts.

        Returns:
            list of (thinking, response) tuples where thinking is "" when absent.
        """
        formatted = [
            self.tokenizer.apply_chat_template(
                msgs,
                tokenize=False,
                add_generation_prompt=True,
                enable_thinking=THINKING,
            )
            for msgs in messages_list
        ]

        logger.info(f"Generating batch of {len(formatted)} prompts...")
        t0 = time.perf_counter()
        outputs = self.llm.generate(formatted, self.sampling_params)
        t_gen = time.perf_counter() - t0

        # Counts all response tokens; both thinking and output
        n_tokens = sum(len(o.outputs[0].token_ids) for o in outputs)
        logger.info(
            f"Generated {n_tokens} tokens in {t_gen:.2f}s ({n_tokens / t_gen:.2f} tok/s)"
        )

        # Detect looping and trim any extra assistant turns before parsing.
        raw_texts = []
        for i, o in enumerate(outputs):
            raw = o.outputs[0].text
            if "assistant" in raw or raw.count("</think>") > 1:
                self._log_loop_tail(raw, o.outputs[0].token_ids, i)
            raw = _trim_extra_turn(raw)
            raw_texts.append(raw)

        # Verify thinking was properly closed when thinking mode is enabled
        if THINKING:
            for i, raw in enumerate(raw_texts):
                if "</think>" not in raw:
                    logger.error(
                        f"Output {i} has no </think> tag — thinking trace may have "
                        f"leaked into response. Raw output (first 200 chars): "
                        f"{repr(raw[:200])}"
                    )

        # Each response becomes tuple of (thinking, output)
        # Branch on thinking, allows us to call _split_thinking with the promise of having
        # <think> and/or </think> tokens in the response
        if THINKING:
            return [_split_thinking(raw) for raw in raw_texts]
        else:
            # Set thinking="" if not enabled
            return [("", raw) for raw in raw_texts]
