"""vLLM engine for batched local inference with Qwen3.6-35B-A3B-FP8."""

import os
import re
import time

from huggingface_hub import snapshot_download

from logger import setup_logging, get_logger
from config import (
    REPO_ID,
    LANGUAGE_MODEL_ONLY,
    SEED,
    THINKING,
    MAX_TOKENS,
    MAX_MODEL_LEN,
    MAX_NUM_BATCHED_TOKENS,
    MAX_NUM_SEQS,
    TEMPERATURE,
    GPU_MEMORY_UTILIZATION,
    TOP_P,
    TOP_K,
    MIN_P,
    PRESENCE_PENALTY,
    REPETITION_PENALTY,
)

setup_logging(log_level=os.getenv("LOG_LEVEL", "debug"))
logger = get_logger(__name__)

_THINK_RE = re.compile(r"<think>(.*?)</think>", re.DOTALL)


def _split_thinking(text):
    """Return (thinking, response) extracted from a raw LLM output.

    Three cases:
      A) Paired <think>...</think> — older GGUF tokenizers that ignored
         enable_thinking, so both tags appear in the generated text.
      B) Only </think> — the FP8 chat template injects the opening
         <think>\\n as part of the assistant prefix, so only the closing
         tag shows up in the output.
      C) Neither tag while THINKING is enabled — generation hit the token
         budget (or got stuck in a loop) before emitting </think>. Treat
         the entire output as thinking and leave response empty so the
         downstream pipeline can detect the overflow.
    """
    if "<think>" in text:
        thinking = "\n".join(m.strip() for m in _THINK_RE.findall(text))
        response = _THINK_RE.sub("", text).strip()
        return thinking, response
    if "</think>" in text:
        thinking, _, response = text.partition("</think>")
        return thinking.strip(), response.strip()
    if THINKING:
        return text.strip(), ""
    return "", text.strip()


class VLLMEngine:
    def __init__(
        self,
        max_tokens=MAX_TOKENS,
        max_model_len=MAX_MODEL_LEN,
        max_num_batched_tokens=MAX_NUM_BATCHED_TOKENS,
        max_num_seqs=MAX_NUM_SEQS,
        temperature=TEMPERATURE,
        seed=SEED,
    ):
        """
        * max_tokens: the maximum number of output tokens that can be generated by a single sequence.
        * max_model_len: the maximum number of input+output tokens a single sequence can occupy.
            - max_model_len * max_num_seqs = #maximum input+output tokens in batch.
            - This is essentially what might blow the KV-cache.
        * max_num_batched_tokens: the maximum number of input tokens for a full batch.
        * max_num_seqs: the maximum batch size.
        """
        from vllm import LLM, SamplingParams

        logger.info(f"Resolving snapshot for {REPO_ID}")
        model_path = snapshot_download(
            repo_id=REPO_ID,
            allow_patterns=["*.safetensors", "*.json", "*.txt", "tokenizer*", "*.py"],
        )
        logger.info(f"Loading model from {model_path}")
        t0 = time.perf_counter()

        llm_kwargs = dict(
            model=model_path,
            max_model_len=max_model_len,
            max_num_seqs=max_num_seqs,
            max_num_batched_tokens=max_num_batched_tokens,
            gpu_memory_utilization=GPU_MEMORY_UTILIZATION,
            seed=seed,
        )
        if LANGUAGE_MODEL_ONLY:
            llm_kwargs["language_model_only"] = True

        try:
            self.llm = LLM(**llm_kwargs)
        except TypeError as e:
            if "language_model_only" not in str(e):
                raise
            logger.warning(
                "language_model_only kwarg unsupported; "
                "falling back to limit_mm_per_prompt={image:0, video:0}"
            )
            llm_kwargs.pop("language_model_only", None)
            llm_kwargs["limit_mm_per_prompt"] = {"image": 0, "video": 0}
            self.llm = LLM(**llm_kwargs)

        logger.info(f"Model loaded in {time.perf_counter() - t0:.2f}s")

        self.tokenizer = self.llm.get_tokenizer()

        # Build stop-token set: include the tokenizer's EOS plus <|im_end|>
        # explicitly.  With some GGUF checkpoints vLLM does not automatically
        # stop at <|im_end|>, causing the model to continue past its natural
        # end-of-turn and re-emit role markers ("assistant") in the output.
        stop_token_ids: list[int] = []
        eos_id = self.tokenizer.eos_token_id
        if eos_id is not None:
            stop_token_ids.append(eos_id)
        try:
            im_end_id = self.tokenizer.convert_tokens_to_ids("<|im_end|>")
            if im_end_id is not None and im_end_id not in stop_token_ids:
                stop_token_ids.append(im_end_id)
                logger.info(f"Added <|im_end|> (id={im_end_id}) as explicit stop token")
        except Exception:
            pass
        logger.info(f"Stop token ids: {stop_token_ids}")

        self.sampling_params = SamplingParams(
            temperature=temperature,
            max_tokens=max_tokens,
            top_p=TOP_P,
            top_k=TOP_K,
            min_p=MIN_P,
            presence_penalty=PRESENCE_PENALTY,
            repetition_penalty=REPETITION_PENALTY,
            seed=seed,
            stop_token_ids=stop_token_ids if stop_token_ids else None,
        )

    def _apply_template(self, messages):
        """Apply chat template, disabling thinking mode if supported."""
        try:
            return self.tokenizer.apply_chat_template(
                messages,
                tokenize=False,
                add_generation_prompt=True,
                enable_thinking=THINKING,
            )
        except TypeError:
            return self.tokenizer.apply_chat_template(
                messages,
                tokenize=False,
                add_generation_prompt=True,
            )

    def generate_batch(self, messages_list):
        """Generate responses for a batch of conversations.

        Args:
            messages_list: list of conversations, each a list of role/content dicts.

        Returns:
            list of (thinking, response) tuples. `response` has thinking
            tokens stripped; `thinking` contains the raw content extracted
            from <think>...</think> blocks (empty string when THINKING is off).
        """
        formatted = [self._apply_template(msgs) for msgs in messages_list]

        logger.info(f"Generating batch of {len(formatted)} prompts...")
        t0 = time.perf_counter()
        outputs = self.llm.generate(formatted, self.sampling_params)
        t_gen = time.perf_counter() - t0

        n_tokens = sum(len(o.outputs[0].token_ids) for o in outputs)
        logger.info(
            f"Generated {n_tokens} tokens in {t_gen:.2f}s ({n_tokens / t_gen:.2f} tok/s)"
        )

        return [_split_thinking(o.outputs[0].text) for o in outputs]
